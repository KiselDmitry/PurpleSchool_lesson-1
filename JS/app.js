const roles = ['admin', 'user', 'menager', 'superuser'];

//Метод reverse()  изменяет исходный массив в обратную сторону
console.log(roles);
roles.reverse();
console.log(roles);

const newRoles = ['sisadmin', 'developer'];

//Метод concat()  конкатинирует
const res = roles.concat(newRoles);
console.log(res);
